    private void addNewProfileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewProfileBtnActionPerformed
        
        String[] userInputs = gatherAllUserInput();
        
        String operationStatus;
        try {
            operationStatus = inventoryControl.registerProfile(userInputs);
            JOptionPane.showMessageDialog(null, operationStatus);  
        } catch (ParseException ex) {
            Logger.getLogger(RegisterMovieProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addNewProfileBtnActionPerformed

    private String[] gatherAllUserInput(){
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String[] userInput = new String[6];
        userInput[0] = newProfileTitleTxt.getText();
        userInput[1] = dateFormat.
                format(newReleaseDate.getDate() );
        userInput[2] = newRatingTxt.getText();
        userInput[3] = newDescriptionTxt.getText();
        userInput[4] = newRuntimeTxt.getText();
        userInput[5] = newSerialcodeTxt.getText();
        
        return userInput;
    }
    
