    private void saveProfileChangeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveProfileChangeBtnActionPerformed
        
        String[] userInputs = gatherAllUserInput();

        String operationStatus;
        try {
            operationStatus = movieRequestHandler.handleRegistrationProfile(userInputs);
            JOptionPane.showMessageDialog(null, operationStatus);
        } catch (ParseException ex) {
            Logger.getLogger(ModifyMovieProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveProfileChangeBtnActionPerformed

    private void searchSerialBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchSerialBtnActionPerformed
        
        String selectedSerial = searchSerialTxt.getText();
        
        String operationStatus = movieRequestHandler.handleRetrieval(selectedSerial);
        existingProfilePanel.setVisible(true);
    }//GEN-LAST:event_searchSerialBtnActionPerformed
    
    private String[] gatherAllUserInput(){
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String[] userInput = new String[6];
        userInput[0] = editProfileTitleTxt.getText();
        userInput[1] = dateFormat.
                format(editReleaseDate.getDate() );
        userInput[2] = editRatingTxt.getText();
        userInput[3] = editDescriptionTxt.getText();
        userInput[4] = editRuntimeTxt.getText();
        userInput[5] = editSerialCodeTxt.getText();
        
        return userInput;
    }
    
    public void fillExistingProfileForm( MovieProfile movieProfile ){
        
        editProfileTitleTxt.setText(movieProfile.getTitle() );
        editReleaseDate.setDate(movieProfile.getReleaseDate() );
        editRatingTxt.setText(movieProfile.getRating() );
        editDescriptionTxt.setText(movieProfile.getDescription() );
        editRuntimeTxt.setText(movieProfile.getRunTime() );
        editSerialCodeTxt.setText(movieProfile.getSerialCode() );
    }
    
